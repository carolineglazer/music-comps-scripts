class Variant:

    def __init__(self, num=None, title=None, location=None, num_of_verses=None, verse_order=[], names=[], places=[], fragment=False):
        self.num = num
        self.title = title
        self.location = location
        self.num_of_verses = num_of_verses
        self.verse_order = verse_order
        self.names = names
        self.places = places
        self.fragment = fragment
